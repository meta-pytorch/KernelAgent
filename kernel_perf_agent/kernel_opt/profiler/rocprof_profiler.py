# Output parsing
# ---------------------------------------------------------------------------


def _parse_stats_csv(csv_path: Path) -> Dict[str, Any]:
    """Parse rocprof --stats CSV to extract kernel durations."""
    if not csv_path.exists():
        return {}

    import csv

    rows: List[Dict[str, str]] = []
    try:
        with csv_path.open(newline="") as f:
            reader = csv.DictReader(f)
            rows = list(reader)
    except Exception:
        return {}

    if not rows:
        return {}

    # rocprof stats CSV columns: Name, Calls, TotalDurationNs, AverageNs, ...
    # Pick the last kernel invocation (after warmup)
    row = rows[-1]
    result: Dict[str, Any] = {}
    for key in ("Name", "Calls", "TotalDurationNs", "AverageNs", "Percentage"):
        if key in row:
            try:
                result[key.lower()] = float(row[key]) if key != "Name" else row[key]
            except (ValueError, TypeError):
                result[key.lower()] = row[key]

    if "averagens" in result:
        result["duration_ns"] = result["averagens"]
        result["duration_ms"] = result["averagens"] / 1e6

    return result


def _parse_pmc_csv(csv_path: Path) -> Dict[str, Any]:
    """Parse rocprof PMC CSV to extract hardware counter values."""
    if not csv_path.exists():
        return {}

    import csv

    rows: List[Dict[str, str]] = []
    try:
        with csv_path.open(newline="") as f:
            reader = csv.DictReader(f)
            rows = list(reader)
    except Exception:
        return {}

    if not rows:
        return {}

    # rocprof PMC CSV columns: Index, KernelName, gpu-id, queue-id,
    # queue-index, pid, tid, grd, wgr, lds, scr, arch_vgpr, accum_vgpr,
    # sgpr, wave_size, sig, obj, <counter1>, <counter2>, ...
    # Aggregate all rows by summing numeric counter columns
    aggregated: Dict[str, float] = {}
    counter_cols = [
        k
        for k in rows[0].keys()
        if k
        not in (
            "Index",
            "KernelName",
            "gpu-id",
            "queue-id",
            "queue-index",
            "pid",
            "tid",
            "sig",
            "obj",
        )
    ]

    for row in rows:
        for col in counter_cols:
            try:
                aggregated[col] = aggregated.get(col, 0.0) + float(row[col])
            except (ValueError, TypeError, KeyError):
                pass

    return aggregated


def _merge_rocprof_outputs(
    stats_csv: Path,
    pmc_csv: Path,
) -> Dict[str, Any]:
    """Merge stats and PMC outputs into a normalized metrics dictionary.

    The returned dict is compatible with the rest of the KernelAgent pipeline
    (same structure expected by RooflineAnalyzer and BottleneckAnalyzer).
    """
    stats = _parse_stats_csv(stats_csv)
    pmc = _parse_pmc_csv(pmc_csv)

    # Raw counters (lower-cased for consistency)
    raw: Dict[str, Any] = {}
    for k, v in pmc.items():
        raw[k.lower()] = v

    # Normalize to our metric keys
    metrics: Dict[str, Any] = {
        "sq_waves": raw.get("sq_waves", 0.0),
        "sq_insts_valu": raw.get("sq_insts_valu", 0.0),
        "sq_insts_salu": raw.get("sq_insts_salu", 0.0),
        "sq_insts_vmem_rd": raw.get("sq_insts_vmem_rd", 0.0),
        "sq_insts_vmem_wr": raw.get("sq_insts_vmem_wr", 0.0),
        "sq_insts_lds": raw.get("sq_insts_lds", 0.0),
        "sq_wait_inst_any": raw.get("sq_wait_inst_any", 0.0),
        "tcc_hit": raw.get("tcc_hit_sum", 0.0),
        "tcc_miss": raw.get("tcc_miss_sum", 0.0),
        "tcc_ea_rdreq": raw.get("tcc_ea_rdreq_sum", 0.0),
        "tcc_ea_wrreq": raw.get("tcc_ea_wrreq_sum", 0.0),
        "fetch_size_kb": raw.get("fetch_size", 0.0),
        "write_size_kb": raw.get("write_size", 0.0),
        # Timing from stats pass
        "duration_ms": stats.get("duration_ms", 0.0),
    }

    # Derived: L2 cache hit rate
    tcc_total = metrics["tcc_hit"] + metrics["tcc_miss"]
    metrics["tcc_cache_hit_rate_pct"] = (
        100.0 * metrics["tcc_hit"] / tcc_total if tcc_total > 0 else 0.0
    )

    # Derived: VALU utilization (fraction of cycles spent on vector ALU)
    total_insts = (
        metrics["sq_insts_valu"]
        + metrics["sq_insts_salu"]
        + metrics["sq_insts_vmem_rd"]
        + metrics["sq_insts_vmem_wr"]
        + metrics["sq_insts_lds"]
    )
    metrics["valu_utilization_pct"] = (
        100.0 * metrics["sq_insts_valu"] / total_insts if total_insts > 0 else 0.0
    )

    # Derived: memory-bound heuristic — fraction of instructions that are memory ops
    vmem_insts = metrics["sq_insts_vmem_rd"] + metrics["sq_insts_vmem_wr"]
    metrics["memory_bound_pct"] = (
        100.0 * vmem_insts / total_insts if total_insts > 0 else 0.0
    )

    # Derive roofline-compatible SOL estimates from counter ratios.
    # These are heuristic approximations since rocprof does not provide
    # "Speed of Light" metrics directly (unlike NCU).
    #
    # Compute SOL: use VALU utilization as a proxy for compute utilization.
    # Memory SOL: use cache miss rate × memory traffic as a bandwidth proxy.
    # Both are normalized to [0, 100].
    metrics["compute_sol_pct"] = min(metrics["valu_utilization_pct"], 100.0)
    metrics["memory_sol_pct"] = min(metrics["memory_bound_pct"], 100.0)

    # Include raw counter dump for debugging / LLM consumption
    metrics["_raw_counters"] = {k: v for k, v in raw.items()}

    return metrics


# ---------------------------------------------------------------------------
# Public helper: load metrics from a previously written JSON file
# ---------------------------------------------------------------------------


def load_rocm_metrics(json_path: Path) -> Dict[str, Any]:
    """Load rocprof metrics from a JSON file produced by :func:`profile_triton_kernel_rocm`.

    Args:
        json_path: Path to the JSON file.

    Returns:
        Metrics dictionary.

    Raises:
        FileNotFoundError: If the file does not exist.
    """
    if not json_path.exists():
        raise FileNotFoundError(f"ROCm metrics JSON not found: {json_path}")
    return json.loads(json_path.read_text())


def metrics_to_prompt_rocm(metrics: Dict[str, Any]) -> str:
    """Convert ROCm metrics dict to a JSON string for LLM prompts.

    Args:
        metrics: Metrics dictionary from :func:`load_rocm_metrics`.

    Returns:
        JSON string (drops internal ``_raw_counters`` key for brevity).
    """
    # Exclude raw counter dump from LLM context (too verbose)
    filtered = {k: v for k, v in metrics.items() if k != "_raw_counters"}
    return json.dumps(filtered, indent=2)
